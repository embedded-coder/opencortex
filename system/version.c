/*includes ------------------------------------------------------------------------------*/
#include "version.h"

/*macros --------------------------------------------------------------------------------*/
#define build_ver  "0.0.1"
#define build_date __DATE__
#define build_time __TIME__

/*typedefs ------------------------------------------------------------------------------*/

/*variables -----------------------------------------------------------------------------*/

/*prototypes ----------------------------------------------------------------------------*/

/*private -------------------------------------------------------------------------------*/

/*public --------------------------------------------------------------------------------*/
char* get_build_version(void)
{
	char* version = build_ver;
	return version;
}

char* get_build_date(void)
{
	char* date = build_date;
	return date;
}

char* get_build_time(void)
{
	char* time = build_time;
	return time;
}

char* get_build_year(void)
{
	char* year = build_date;
	return year + 7;
}
