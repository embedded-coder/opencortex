/*includes ------------------------------------------------------------------------------*/
#include "includes.h"
#include "delay.h"
/*macros --------------------------------------------------------------------------------*/

/*typedefs ------------------------------------------------------------------------------*/

/*variables -----------------------------------------------------------------------------*/
static volatile uint32_t delay;

/*prototypes ----------------------------------------------------------------------------*/

/*private -------------------------------------------------------------------------------*/

/*public --------------------------------------------------------------------------------*/

 
void delay_init(uint32_t ticks_per_second)
{
	SysTick_Config(SystemCoreClock / ticks_per_second);	
}


void delay_decrease(void)
{
	if (delay != 0x00)
	{ 
		delay--;
	}
}

void delay_ms(uint32_t ms)
{ 
	delay = ms;

	while(delay != 0);
}


